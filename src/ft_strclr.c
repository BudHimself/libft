#include "../include/libft.h"

void		ft_strclr(char *s)
{
	int		i;
	int		j;
	char	*tmp;

	i = -1;
	j = 0;
	while (s[j] != '\0')
		j++;
	tmp = s;
	while (tmp[++i] && j-- > 0)
		tmp[i] = '\0';
}
