#include "../include/libft.h"

BOOL			ft_strsrch(char *s1, char *s2)
{
	int			i;
	int			j;

	j = -1;
	while (s2[++j])
	{
		i = -1;
		while (s1[++i])
			if (s1[i] == s2[j])
				return (1);
	}
	return (0);
}
