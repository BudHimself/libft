#include "../include/libft.h"

void			get_sign(t_data *data, char sign[1][2])
{
	sign[0][0] = '\0';
	sign[0][0] = (PLUS) ? '+' : sign[0][0];
	sign[0][0] = (SPACE) ? ' ' : sign[0][0];
	sign[0][0] = (SIGN == '-') ? '-' : sign[0][0];
	sign[0][1] = '\0';
}
